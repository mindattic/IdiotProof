namespace IdiotProof.Enums
{
    /// <summary>
    /// Comparison operators for evaluating technical indicator values.
    /// </summary>
    public enum Comparison
    {
        /// <summary>
        /// Greater than or equal to (>=)
        /// </summary>
        Gte,

        /// <summary>
        /// Less than or equal to (&lt;=)
        /// </summary>
        Lte,

        /// <summary>
        /// Greater than (>)
        /// </summary>
        Gt,

        /// <summary>
        /// Less than (&lt;)
        /// </summary>
        Lt,

        /// <summary>
        /// Equal to (==)
        /// </summary>
        Eq
    }
}
